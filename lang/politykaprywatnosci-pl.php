<ol>
<li>Wstęp</li>
</ol>
<p>Firma Expandio (zwana dalej &bdquo;my&rdquo;, &bdquo;nas&rdquo; lub &bdquo;nasza&rdquo;) szanuje prywatność użytkownik&oacute;w strony internetowej expandio.pl (&bdquo;Serwis&rdquo;) i zobowiązuje się do jej ochrony. Niniejsza Polityka Prywatności wyjaśnia, jakie informacje zbieramy, jak je wykorzystujemy oraz jak je chronimy. Korzystając z naszego Serwisu, zgadzasz się na postanowienia zawarte w niniejszej Polityce Prywatności.</p>
<ol start="2">
<li>Jakie informacje zbieramy?</li>
</ol>
<p>Podczas korzystania z naszego Serwisu możemy zbierać informacje, kt&oacute;re możemy wykorzystać do identyfikacji użytkownika, takie jak:</p>
<p>a) Dane osobowe, kt&oacute;re użytkownik podaje dobrowolnie, takie jak imię, nazwisko, adres e-mail, numer telefonu, dane do płatności.</p>
<p>b) Dane związane z korzystaniem z naszego Serwisu, takie jak adres IP, typ przeglądarki, informacje o systemie operacyjnym, informacje o odwiedzanych stronach, czas spędzony na stronie, daty i godziny korzystania z Serwisu.</p>
<p>c) Dane związane z komunikacją, takie jak wiadomości e-mail, kt&oacute;re użytkownik wysyła do nas.</p>
<ol start="3">
<li>Jak wykorzystujemy zebrane informacje?</li>
</ol>
<p>Zebrane informacje wykorzystujemy w celu:</p>
<p>a) Zapewnienia, utrzymania i ulepszania naszego Serwisu.</p>
<p>b) Analizowania korzystania z naszego Serwisu w celu ulepszania funkcjonalności i oferowanych usług.</p>
<p>c) Komunikacji z użytkownikami, w tym udzielania odpowiedzi na pytania, wysyłania informacji o nowościach, promocjach, badaniach rynkowych.</p>
<p>d) Zapewnienia bezpieczeństwa naszego Serwisu i ochrony przed nieautoryzowanym dostępem.</p>
<ol start="4">
<li>Jak chronimy zebrane informacje?</li>
</ol>
<p>Stosujemy odpowiednie środki techniczne i organizacyjne, aby chronić zebrane informacje przed nieuprawnionym dostępem, utratą, zmianą lub zniszczeniem. Szyfrowanie danych, zabezpieczenia serwer&oacute;w oraz ograniczenie dostępu do danych osobowych tylko dla upoważnionych pracownik&oacute;w to niekt&oacute;re z metod, kt&oacute;re stosujemy w celu ochrony prywatności naszych użytkownik&oacute;w.</p>
<ol start="5">
<li>Udostępnianie danych osobowych</li>
</ol>
<p>Nie sprzedajemy ani nie udostępniamy danych osobowych naszych użytkownik&oacute;w osobom trzecim, chyba że:</p>
<p>a) Uzyskamy uprzednią zgodę użytkownika.</p>
<p>b) W przypadku, gdy jest to wymagane przez prawo, np. na żądanie organ&oacute;w ścigania.</p>
<p>c) W przypadku, gdy jest to konieczne w celu ochrony naszych praw, własności lub bezpieczeństwa.</p>
<p>Expandio zbiera r&oacute;wnież adresy e-mail użytkownik&oacute;w za pośrednictwem formularzy zamieszczonych na stronie. Gromadzenie tych danych ma na celu umożliwienie przesyłania informacji handlowych, promocyjnych oraz innych treści marketingowych poprzez newsletter. W celu przechowywania i zarządzania tymi danymi, korzystamy z usług MailerLite, zewnętrznego dostawcy usług marketingowych. Przekazując sw&oacute;j adres e-mail za pośrednictwem formularza, użytkownik wyraża zgodę na przekazanie swoich danych do MailerLite i na otrzymywanie wiadomości e-mail od Expandio. Więcej informacji na temat MailerLite oraz polityki prywatności tego dostawcy można znaleźć na stronie <a href="https://www.mailerlite.com/legal/" target="_new">https://www.mailerlite.com/legal/</a>.</p>
<ol start="6">
<li>Prawa użytkownik&oacute;w</li>
</ol>
<p>Użytkownik ma prawo do dostępu</p>
<p>do swoich danych osobowych, ich sprostowania, usunięcia lub ograniczenia przetwarzania. Ponadto, użytkownik ma prawo do wniesienia sprzeciwu wobec przetwarzania jego danych oraz prawo do przenoszenia danych. W celu realizacji tych praw, prosimy o kontakt z nami za pośrednictwem danych kontaktowych podanych w punkcie 8 niniejszej Polityki Prywatności.</p>
<p>Użytkownik ma r&oacute;wnież prawo do wniesienia skargi do właściwego organu nadzorczego, jeśli uważa, że przetwarzanie jego danych osobowych narusza przepisy o ochronie danych.</p>
<ol start="7">
<li>Cookies</li>
</ol>
<p>Nasz Serwis używa plik&oacute;w cookie, kt&oacute;re są małymi plikami tekstowymi przechowywanymi na komputerze użytkownika, służącymi do identyfikacji przeglądarki podczas korzystania z Serwisu. Pliki cookie pomagają nam w analizie korzystania z Serwisu, personalizacji treści i reklam oraz monitorowaniu efektywności naszych działań marketingowych. Użytkownik może zablokować pliki cookie w ustawieniach swojej przeglądarki, jednak może to wpłynąć na funkcjonalność naszego Serwisu.</p>
