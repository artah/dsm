<ol start="10">
<li>Akceptacja Polityki Prywatności</li>
</ol>
<p>Korzystając z naszego Serwisu, użytkownik akceptuje postanowienia niniejszej Polityki Prywatności. Jeśli użytkownik nie zgadza się z postanowieniami zawartymi w Polityce Prywatności, prosimy o natychmiastowe zaprzestanie korzystania z naszego Serwisu.</p>
